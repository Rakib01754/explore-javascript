//---------- declaring variable value for the first time----------- //

var potatoPrice = 20;

//------------ variable value update --------------//
potatoPrice = 25;

// -------------adding with previous value ---------------//

potatoPrice = potatoPrice + 10;

// console.log(potatoPrice); 

//------------ adding with previous value shorthand------------ //
potatoPrice += 140;

// console.log(potatoPrice); 

// -------------- deducting value shorthand -----------//

potatoPrice -= 100;

// console.log(potatoPrice); 

// ---------multiplication shorthand----------------- //

potatoPrice *= 2;

// console.log(potatoPrice); 

//---------------- divide short hand ----------------//

potatoPrice /= 15;

// console.log(potatoPrice); 

//---------------adding 1 to the previous value(++) -------------//

var age = 25;

age++;
// console.log(age); 

//------------ deduction 1 from previous value (--) -------------//
age--;

